package reference;

public class Subject {
    String subjectName;
    int score;

    public void setSubjectName(String aname) {
        subjectName = aname;
    }

    public String getSubjectName() {
        return subjectName;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getScore() {
        return score;
    }

}
